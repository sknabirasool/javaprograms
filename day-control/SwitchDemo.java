public class SwitchDemo {
    public static void main(String[] args) {
        
        int number=10;
        switch(number)//10
        {
            case 1:
            System.out.println("1");
            break;
            case 2:
            System.out.println("2");
            break;
            case 10:
            System.out.println("10");
            break;
            default:
            System.out.println("not Availble in 1 2 10");

        }


    }
    
}

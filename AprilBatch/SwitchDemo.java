package AprilBatch;

public class SwitchDemo {
    public static void main(String[] args) {
        int num=10;

        switch (num) {
           
            case 20:
            System.out.println("20"); 
            break; 
            case 30:
            System.out.println("20");    
            break;    
            default:  System.out.println("not found");    
                break;
        }
    }
    
}

public class RelationDemo
{
    public static void main(String args[])
    {
        int a=7;
        int b=5;

        System.out.println("Equal Operator "+(a==b)); //7==5
        System.out.println("Equal != "+(a!=b));
        System.out.println("Equal > "+(a>b));
        System.out.println("Equal < "+(a<b));
        System.out.println("Equal >= "+(a>=b)); 
        System.out.println("Equal <= "+(a<=b));


    }
}